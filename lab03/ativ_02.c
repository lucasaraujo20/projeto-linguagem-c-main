#include <stdio.h>
#include <stdlib.h>
#include <math.h>


int main(){


printf(" \n for \n \n");

    int x ;

    for( x = 1; x <= 100 ; x++){

        printf("%d \n", x);
    }



printf(" \n Vamos para o while \n \n");
//system("pause \n");

int j = 1;

while( j <= 100 ){

printf("%d \n", j);

j++;



}

printf(" \n Vamos para o do-while \n \n");
//system("pause \n");

int d = 1;

do{
    printf("%d \n", d);

    d++;

}while( d <= 100);


return 0;
}